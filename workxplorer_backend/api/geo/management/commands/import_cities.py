class Command(BaseCommand):
    help = "Импортирует города (рус/англ, координаты), устраняет дубли"

    def handle(self, *args, **options):
        created = 0
        updated = 0

        for country, cc, city, lat, lon in CITIES:
            # Нормализация
            country_clean = COUNTRY_NORMALIZATION.get(country.strip(), country.strip())
            city_clean = city.strip()

            name_latin = unidecode(city_clean).lower()
            point = Point(lon, lat)

            # === 1) Поиск дублей по нормализованному латинскому имени ===
            existing = GeoPlace.objects.filter(
                name_latin=name_latin, country_code=cc.upper()
            ).first()

            # === 2) Поиск дублей по координатам (точное совпадение) ===
            if not existing:
                existing = GeoPlace.objects.filter(point=point, country_code=cc.upper()).first()

            # === 3) Поиск дублей по имени (регистронезависимый поиск) ===
            if not existing:
                existing = GeoPlace.objects.filter(
                    name__iexact=city_clean, country_code=cc.upper()
                ).first()

            # ===== Если нашли существующий — обновляем =====
            if existing:
                existing.country = country_clean
                existing.name_latin = name_latin
                existing.point = point
                existing.raw = {}
                existing.provider = "import"
                existing.save()

                updated += 1
                continue

            # ===== Если не найден — создаём =====
            GeoPlace.objects.create(
                name=city_clean,
                country=country_clean,
                country_code=cc.upper(),
                name_latin=name_latin,
                point=point,
                provider="import",
                raw={},
            )

            created += 1

        self.stdout.write(
            self.style.SUCCESS(f"Импорт завершён: создано {created}, обновлено {updated}.")
        )
